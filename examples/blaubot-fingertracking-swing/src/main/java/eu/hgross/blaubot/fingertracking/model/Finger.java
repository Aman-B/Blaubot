package eu.hgross.blaubot.fingertracking.model;


public class Finger {
	private float x = 0;
	private float y = 0;
	private int color = 0;
	
	
	public Finger() {
		
	}

	public float getX() {
		return x;
	}

	public void setX(float x) {
		this.x = x;
	}

	public float getY() {
		return y;
	}

	public void setY(float y) {
		this.y = y;
	}

	public int getColor() {
		return color;
	}

	public void setColor(int color) {
		this.color = color;
	}

}
